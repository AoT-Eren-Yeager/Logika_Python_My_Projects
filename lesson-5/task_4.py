# Напиши програму, яка робить запит двох чисел, перемножує їх і друкує результат.
# Програма робить запит на перше і друге число, переводить відповіді в числа, перемножує їх і друкує результат. Не потрібно друкувати ніяких слів - тільки число, яке вийде.
a = int(input("Введіть перше число: "))
b = int(input("Введіть друге число: "))
c = (a * b)
print("Сума: ", a, "*", b, "=", c)